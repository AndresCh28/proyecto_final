begin
    update public.usuarios
       set correo = coalesce(new.email, correo),
           fecha_actualizacion = now()
     where auth_user_id = new.id;

    return new;
end;
$$;

create or replace function public.registrar_historial_generico()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
    v_payload jsonb;
    v_id_registro bigint;
begin
    v_payload := case when tg_op = 'DELETE' then to_jsonb(old) else to_jsonb(new) end;
    v_id_registro := coalesce(nullif(v_payload ->> tg_argv[0], ''), '0')::bigint;

    insert into public.historial_cambios (
        tabla_afectada,
        id_registro,
        accion,
        realizado_por,
        descripcion
    )
    values (
        tg_table_name,
        v_id_registro,
        tg_op,
        public.get_current_internal_user_id(),
        coalesce(tg_argv[1], tg_table_name || ' ' || lower(tg_op))
    );

    return case when tg_op = 'DELETE' then old else new end;
end;
$$;

create or replace function public.registrar_cambio_estado_comision()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
