create or replace function public.actualizar_resultado_propuesta(p_id_propuesta bigint)
returns table (
    id_propuesta bigint,
    votos_a_favor integer,
    votos_en_contra integer,
    abstenciones integer,
    resultado_calculado varchar(20)
)
language plpgsql
security definer
set search_path = public
as $$
declare
    v_favor integer;
    v_contra integer;
    v_abstencion integer;
    v_resultado varchar(20);
begin
    select
        count(*) filter (where voto = 'a_favor'),
        count(*) filter (where voto = 'en_contra'),
        count(*) filter (where voto = 'abstencion')
    into v_favor, v_contra, v_abstencion
    from public.votos
    where public.votos.id_propuesta = p_id_propuesta;

    if v_favor > v_contra then
        v_resultado := 'aprobada';
    elsif v_contra > v_favor then
        v_resultado := 'rechazada';
    else
        v_resultado := 'empate';
    end if;

    update public.propuestas
       set resultado_final = v_resultado,
           estado = case
               when v_resultado = 'aprobada' then 'aprobada'
               when v_resultado = 'rechazada' then 'rechazada'
               else estado
           end,
           fecha_cierre = case
               when v_resultado in ('aprobada', 'rechazada') then coalesce(fecha_cierre, now())
               else fecha_cierre
           end
     where public.propuestas.id_propuesta = p_id_propuesta;

    return query
    select p_id_propuesta, v_favor, v_contra, v_abstencion, v_resultado;
end;
$$;

