create or replace function public.get_current_internal_user_id()
returns bigint
language sql
stable
security definer
set search_path = public
as $$
    select u.id_usuario
    from public.usuarios u
    where u.auth_user_id = auth.uid()
      and u.activo = true
    limit 1;
$$;

create or replace function public.user_has_role(target_roles text[])
returns boolean
language sql
stable
security definer
set search_path = public
as $$
    select coalesce((
        select r.nombre = any(target_roles)
        from public.usuarios u
        join public.roles r on r.id_rol = u.id_rol
        where u.auth_user_id = auth.uid()
          and u.activo = true
        limit 1
    ), false);
$$;

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
    select public.user_has_role(array['Administrador']);
$$;

create or replace function public.is_admin_or_coordinator()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
    select public.user_has_role(array['Administrador', 'Coordinador']);
$$;

create or replace function public.is_comision_member(p_id_comision bigint)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
    select coalesce((
        select true
        from public.comision_miembros cm
        where cm.id_comision = p_id_comision
          and cm.id_usuario = public.get_current_internal_user_id()
          and cm.activo = true
        limit 1
    ), false);
$$;

create or replace function public.get_comision_id_from_propuesta(p_id_propuesta bigint)
returns bigint
language sql
stable
security definer
set search_path = public
as $$
    select p.id_comision
    from public.propuestas p
    where p.id_propuesta = p_id_propuesta
    limit 1;
$$;

create or replace function public.can_vote_on_propuesta(p_id_propuesta bigint)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
    select
        public.is_admin_or_coordinator()
        or exists (
            select 1
            from public.propuestas p
            join public.comision_miembros cm
                on cm.id_comision = p.id_comision
               and cm.id_usuario = public.get_current_internal_user_id()
               and cm.activo = true
               and cm.puede_votar = true
            where p.id_propuesta = p_id_propuesta
              and p.estado = 'pendiente'
        );
$$;

create or replace function public.can_manage_archivo(p_id_comision bigint, p_id_propuesta bigint)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
    select
        public.is_admin_or_coordinator()
        or (
            p_id_comision is not null
            and public.is_comision_member(p_id_comision)
        )
        or (
            p_id_propuesta is not null
            and public.is_comision_member(public.get_comision_id_from_propuesta(p_id_propuesta))
        );
$$;

create or replace function public.handle_new_auth_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
    v_id_rol_miembro smallint;
    v_nombre text;
begin
    select id_rol
    into v_id_rol_miembro
    from public.roles
    where nombre = 'Miembro'
    limit 1;

    v_nombre := coalesce(
        new.raw_user_meta_data ->> 'nombre',
        new.raw_user_meta_data ->> 'full_name',
        split_part(new.email, '@', 1)
    );

    insert into public.usuarios (
        auth_user_id,
        nombre,
        correo,
        telefono,
        id_rol,
        activo
    )
    values (
        new.id,
        v_nombre,
        new.email,
        new.raw_user_meta_data ->> 'telefono',
        v_id_rol_miembro,
        true
    );

    return new;
end;
$$;

