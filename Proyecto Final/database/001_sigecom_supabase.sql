create view public.vw_balance_comisiones as
select
    c.id_comision,
    c.titulo,
    e.nombre as estado,
    coalesce(p.monto_estimado, 0) as monto_estimado,
    coalesce(p.monto_aprobado, 0) as monto_aprobado,
    coalesce(sum(case when mf.tipo = 'ingreso' then mf.monto else 0 end), 0) as total_ingresos,
    coalesce(sum(case when mf.tipo = 'gasto' then mf.monto else 0 end), 0) as total_gastos,
    coalesce(sum(case when mf.tipo = 'ingreso' then mf.monto else 0 end), 0)
        - coalesce(sum(case when mf.tipo = 'gasto' then mf.monto else 0 end), 0) as balance_actual
from public.comisiones c
join public.estados e on e.id_estado = c.id_estado
left join public.presupuestos p on p.id_comision = c.id_comision
left join public.movimientos_financieros mf on mf.id_comision = c.id_comision
group by c.id_comision, c.titulo, e.nombre, p.monto_estimado, p.monto_aprobado;

create view public.vw_resultados_propuestas as
select
    p.id_propuesta,
    p.id_comision,
    p.titulo,
    p.estado,
    p.resultado_final,
    count(v.id_voto) filter (where v.voto = 'a_favor') as votos_a_favor,
    count(v.id_voto) filter (where v.voto = 'en_contra') as votos_en_contra,
    count(v.id_voto) filter (where v.voto = 'abstencion') as abstenciones,
    case
        when count(v.id_voto) filter (where v.voto = 'a_favor')
           > count(v.id_voto) filter (where v.voto = 'en_contra') then true
        else false
    end as aprobada_por_mayoria_simple
from public.propuestas p
left join public.votos v on v.id_propuesta = p.id_propuesta
group by p.id_propuesta, p.id_comision, p.titulo, p.estado, p.resultado_final;

create view public.vw_actividad_comisiones as
select
    c.id_comision,
    c.titulo,
    count(distinct pr.id_propuesta) as cantidad_propuestas,
    count(distinct ar.id_archivo) as cantidad_archivos,
    count(distinct mf.id_movimiento) as cantidad_movimientos,
    count(distinct cm.id_usuario) as cantidad_miembros
from public.comisiones c
left join public.propuestas pr on pr.id_comision = c.id_comision
left join public.archivos ar on ar.id_comision = c.id_comision
left join public.movimientos_financieros mf on mf.id_comision = c.id_comision
left join public.comision_miembros cm on cm.id_comision = c.id_comision and cm.activo = true
group by c.id_comision, c.titulo;

