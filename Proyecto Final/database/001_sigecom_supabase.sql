create table public.roles (
    id_rol smallint generated by default as identity primary key,
    nombre varchar(50) not null unique,
    descripcion text,
    fecha_creacion timestamptz not null default now()
);

create table public.estados (
    id_estado smallint generated by default as identity primary key,
    nombre varchar(50) not null unique,
    descripcion text,
    fecha_creacion timestamptz not null default now()
);

create table public.usuarios (
    id_usuario bigint generated by default as identity primary key,
    auth_user_id uuid unique references auth.users (id) on delete cascade,
    nombre varchar(120) not null,
    correo varchar(255) not null unique,
    telefono varchar(30),
    id_rol smallint not null references public.roles (id_rol),
    activo boolean not null default true,
    fecha_creacion timestamptz not null default now(),
    fecha_actualizacion timestamptz not null default now(),
    constraint usuarios_correo_formato_chk check (position('@' in correo) > 1)
);

create table public.comisiones (
    id_comision bigint generated by default as identity primary key,
    titulo varchar(150) not null,
    descripcion text,
    fecha_inicio date not null,
    fecha_fin date,
    id_estado smallint not null references public.estados (id_estado),
    creado_por bigint references public.usuarios (id_usuario),
    fecha_creacion timestamptz not null default now(),
    fecha_actualizacion timestamptz not null default now(),
    constraint comisiones_fechas_chk check (fecha_fin is null or fecha_fin >= fecha_inicio)
);

create table public.comision_miembros (
    id_comision bigint not null references public.comisiones (id_comision) on delete cascade,
    id_usuario bigint not null references public.usuarios (id_usuario) on delete cascade,
    puede_votar boolean not null default true,
    es_responsable boolean not null default false,
    activo boolean not null default true,
    fecha_asignacion timestamptz not null default now(),
    primary key (id_comision, id_usuario)
);

create table public.presupuestos (
    id_presupuesto bigint generated by default as identity primary key,
    id_comision bigint not null unique references public.comisiones (id_comision) on delete cascade,
    monto_estimado numeric(12, 2) not null default 0,
    monto_aprobado numeric(12, 2) default 0,
    fecha_aprobacion date,
    observaciones text,
    creado_por bigint references public.usuarios (id_usuario),
    fecha_creacion timestamptz not null default now(),
    fecha_actualizacion timestamptz not null default now(),
    constraint presupuestos_monto_estimado_chk check (monto_estimado >= 0),
    constraint presupuestos_monto_aprobado_chk check (monto_aprobado is null or monto_aprobado >= 0)
);

create table public.movimientos_financieros (
    id_movimiento bigint generated by default as identity primary key,
    id_comision bigint not null references public.comisiones (id_comision) on delete cascade,
    tipo varchar(20) not null,
    monto numeric(12, 2) not null,
    descripcion text not null,
    fecha timestamptz not null default now(),
    creado_por bigint references public.usuarios (id_usuario),
    fecha_creacion timestamptz not null default now(),
    constraint movimientos_tipo_chk check (tipo in ('ingreso', 'gasto')),
    constraint movimientos_monto_chk check (monto > 0)
);

create table public.propuestas (
    id_propuesta bigint generated by default as identity primary key,
    id_comision bigint not null references public.comisiones (id_comision) on delete cascade,
    titulo varchar(150) not null,
    descripcion text not null,
    fecha_creacion timestamptz not null default now(),
    estado varchar(20) not null default 'pendiente',
    resultado_final varchar(20) not null default 'pendiente',
    fecha_cierre timestamptz,
    creada_por bigint references public.usuarios (id_usuario),
    constraint propuestas_estado_chk check (estado in ('pendiente', 'aprobada', 'rechazada', 'cerrada')),
    constraint propuestas_resultado_chk check (resultado_final in ('pendiente', 'aprobada', 'rechazada', 'empate'))
);

create table public.votos (
    id_voto bigint generated by default as identity primary key,
    id_propuesta bigint not null references public.propuestas (id_propuesta) on delete cascade,
    id_usuario bigint not null references public.usuarios (id_usuario) on delete cascade,
    voto varchar(20) not null,
    fecha timestamptz not null default now(),
    constraint votos_tipo_chk check (voto in ('a_favor', 'en_contra', 'abstencion')),
    constraint votos_unicos_por_usuario unique (id_propuesta, id_usuario)
);

create table public.reportes (
    id_reporte bigint generated by default as identity primary key,
    id_comision bigint references public.comisiones (id_comision) on delete set null,
    generado_por bigint not null references public.usuarios (id_usuario),
    tipo varchar(50) not null default 'general',
    fecha_generacion timestamptz not null default now(),
    descripcion text,
    constraint reportes_tipo_chk check (tipo in ('presupuestos', 'financiero', 'propuestas', 'actividad_comisiones', 'general'))
);

create table public.notificaciones (
    id_notificacion bigint generated by default as identity primary key,
    id_usuario bigint not null references public.usuarios (id_usuario) on delete cascade,
    mensaje text not null,
    leida boolean not null default false,
    tipo varchar(50) not null default 'informativa',
    fecha_envio timestamptz not null default now()
);

create table public.archivos (
    id_archivo bigint generated by default as identity primary key,
    id_comision bigint references public.comisiones (id_comision) on delete cascade,
    id_propuesta bigint references public.propuestas (id_propuesta) on delete cascade,
    nombre varchar(255) not null,
    bucket varchar(100) not null default 'sigecom-archivos',
    ruta_storage text not null unique,
    url text,
    mime_type varchar(100),
    tamano_bytes bigint,
    fecha_subida timestamptz not null default now(),
    subido_por bigint references public.usuarios (id_usuario),
    constraint archivos_relacion_chk check (id_comision is not null or id_propuesta is not null),
    constraint archivos_tamano_chk check (tamano_bytes is null or tamano_bytes >= 0)
);

create table public.historial_cambios (
    id_historial bigint generated by default as identity primary key,
    tabla_afectada varchar(100) not null,
    id_registro bigint not null,
    accion varchar(30) not null,
    realizado_por bigint references public.usuarios (id_usuario),
    descripcion text,
    fecha timestamptz not null default now(),
    constraint historial_accion_chk check (
        accion in ('INSERT', 'UPDATE', 'DELETE', 'LOGIN', 'LOGOUT', 'CAMBIO_ESTADO', 'VOTO', 'SUBIDA_ARCHIVO')
    )
);

